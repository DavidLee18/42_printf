#include "ft_printf.h"

int	is_conv_spec(const char c)
{
	return (c == 'c' || c == 's'
		|| c == 'p' || c == 'd' || c == 'i'
		|| c == 'u' || c == 'x' || c == 'X'
		|| c == '%');
}
